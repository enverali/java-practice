package Month1;

public class loops_example {

	public static void main(String[] args) {

		int i =1;
		int j =1;
		
		do {
			
			System.out.println("Number I is " + i);
			i++;
		}while(i<=10);
		
		while(j<=10){
			
			System.out.println("Number J is: " + j);
			j=j+1;
			
		}
		
		for(int x=1; x<=10; x++) {
			
			System.out.println("Value of X is: " + x);
			
		}
		
	}
}

